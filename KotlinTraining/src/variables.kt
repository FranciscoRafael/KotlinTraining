fun main() {

    // basic types
    var juros: Double = 16.1
    var idade: Int = 10

    var torf: Boolean = false

    var character: Char = 'C'

    var nome: String = "Francisco"

    println(juros)
    println(idade)
    println(torf)
    println(character)
    println(nome)


    //constants and changing variables

    //mut variables
    var newnome: String = "Francisco Carneiro"
    println(newnome)
    newnome = "New valor"
    println(newnome)

    // imut variables
    val y: Int

    y = 20

    println(y)

}